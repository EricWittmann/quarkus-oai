package io.wittmann.beans;

public class Widget {
	
	private String name;
	private int size;
	
	public Widget() {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}

}
